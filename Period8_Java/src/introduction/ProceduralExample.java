package introduction;

public class ProceduralExample {
  public static void main(String[] args){
	  String[] names={"Jillian","Joseph","Jordan"};
	  String[] years={"Senior","Junior","Senior"};
	  System.out.println("Hi, my name is "+names[0]+" and I'm a "+years[0]+".");
	  System.out.println("Hi, my name is "+names[1]+" and I'm a "+years[1]+".");
	  System.out.println("Hi, my name is "+names[2]+" and I'm a "+years[2]+".");
  }
}
